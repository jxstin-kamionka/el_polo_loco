/**
 * Shared keyboard state used by desktop and mobile controls.
 */
class Keyboard {
  /** @type {boolean} Left movement key state. */
  LEFT = false;

  /** @type {boolean} Right movement key state. */
  RIGHT = false;

  /** @type {boolean} Jump key state. */
  SPACE = false;

  /** @type {boolean} Up key state. */
  UP = false;

  /** @type {boolean} Down key state. */
  DOWN = false;

  /** @type {boolean} Throw key state. */
  D = false;
}
